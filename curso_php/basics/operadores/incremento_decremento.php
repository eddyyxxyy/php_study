<?php

// Operadores de incremento e decremento

$number = 10;

// O incremento altera o valor da variável.
// ++ soma um, é o incremento.
echo ++$number; // pré-incremento
echo $number++; // pós-incremento

// -- subtrai um, é o decremento
echo --$number; // pré-decremento
echo $number--; // pós-decremento

// Nesse caso:
// O pré incremento/decremento altera a variável antes de ser mostrada
// Já o pós incremento/decremento só altera o valor depois de ter mostrado